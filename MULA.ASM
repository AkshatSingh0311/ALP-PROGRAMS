;PRODUCT OF NUMBERS IN AN ARRAY
.model-smDLl 
.stack 100h
.data
array DB 08,04,01,03,05,06
LEN DW 0006
.code
MOV AX,@data
MOV DS,AX


LEA SI,array
MOV DL,[SI]
ADD LEN,SI
MOV BX,LEN
MOV AX,1
SUM:
	MUL DL
	INC SI
	MOV DL,[SI]
	JMP LENCHK
LENCHK: ;CHECKING THE ITERATIONS 
	CMP BX,SI ;IS LEN=>SI 
	JA SUM ;YES
	JMP EXIT
EXIT:
	INT 03H 
END
