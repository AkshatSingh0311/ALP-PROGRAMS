;FINDING THE SMALLEST NUMBER IN AN ARRAY
.model-small 
.stack 100h
.data
array DB 08,04,01,03,05,06
LEN DW 0006
.code
MOV AX,@data
MOV DS,AX


LEA SI,array
MOV AL,[SI]
ADD LEN,SI
MOV BX,LEN

CHK: 
	CMP AL,[SI+1] ;IS AL > SI+1?
	JB  BIGGER   ;NO 
	JMP SMALLER
SMALLER: ;NEXT NUMBER IS LESS THAN THE CURRENT VALUE 
	MOV AL,[SI+1] 
	INC SI
	JMP LENCHK
BIGGER: ;NEXT NUMBER IS GREATER THAN THE CURRENT VALUE 
	INC SI
	JMP LENCHK 
LENCHK: ;CHECKING THE ITERATIONS 
	CMP BX,SI ;IS LEN=>SI 
	JA CHK ;YES
	JMP EXIT
EXIT:
	INT 03H 
END
