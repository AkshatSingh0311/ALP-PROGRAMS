.model-small 
.stack 100h
.data
array DB 08,04,01,03,05,06,10
LEN DW 0007
.code
MOV AX,@data
MOV DS,AX


LEA SI,array
MOV AL,[SI]
ADD LEN,SI
MOV BX,LEN
SUB BX,1
CHK: 
	CMP [SI+1],AL ;IS SI+1>AL ?
	MOV DX,[SI+1]
	JB  SMALLER   ;NO 
	JMP BIGGER
SMALLER: ;NEXT NUMBER IS LESS THAN THE CURRENT VALUE 
	INC SI
	JMP LENCHK 
BIGGER: ;NEXT NUMBER IS GREATER THAN THE CURRENT VALUE 
	MOV AL,[SI+1] 
	MOV DX,[SI+1]
	INC SI
	JMP LENCHK
LENCHK: ;CHECKING THE ITERATIONS 
	CMP BX,SI ;IS LEN=>SI 
	JA CHK ;YES
	JMP EXIT
EXIT:
	INT 03H 
END