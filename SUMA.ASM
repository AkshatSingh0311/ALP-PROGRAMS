.stack 100h
.data
array DB 08,04,01,03,05,06
LEN DW 0006
.code
MOV AX,@data
MOV DS,AX


LEA SI,array
MOV AL,[SI]
ADD LEN,SI
MOV BX,LEN
MOV CL,0
SUM:
	ADD CL,AL
	INC SI
	MOV AL,[SI]
	JMP LENCHK
LENCHK: ;CHECKING THE ITERATIONS 
	CMP BX,SI ;IS LEN=>SI 
	JA SUM ;YES
	JMP EXIT
EXIT:
	INT 03H 
END